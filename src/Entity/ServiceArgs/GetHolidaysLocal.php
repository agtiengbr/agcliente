<?php

namespace AGTI\Cliente\Entity\ServiceArgs;

class GetHolidaysLocal
{
    protected $year;

    /**
     * Get the value of year
     */ 
    public function getYear()
    {
        return $this->year;
    }

    /**
     * Set the value of year
     *
     * @return  self
     */ 
    public function setYear($year)
    {
        $this->year = $year;

        return $this;
    }
}